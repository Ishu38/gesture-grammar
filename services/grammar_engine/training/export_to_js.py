# ---------------------------------------------------------------------------
# 4. Fusion weights → JS const
# ---------------------------------------------------------------------------

def export_fusion_weights() -> str | None:
    """Export fusion weights as a JavaScript const declaration."""
    logger.info("=== Exporting Fusion Weights ===")

    emission_path = MODELS_DIR / "emission_matrices.json"
    if not emission_path.exists():
        return None

    with open(emission_path) as f:
        data = json.load(f)

    fw = data.get("fusion_weights", {}).get("best_weights")
    if not fw:
        return None

    lines = [
        "/**",
        " * Multimodal Fusion Weights — optimized via grid search",
        " * Generated by MLAF training pipeline",
        " */",
        "export const FUSION_WEIGHTS = {",
        f"  visual: {fw.get('w_visual', 0.6):.4f},",
        f"  acoustic: {fw.get('w_acoustic', 0.2):.4f},",
        f"  gaze: {fw.get('w_gaze', 0.2):.4f},",
        "};",
    ]

    js_code = "\n".join(lines)

    path = MODELS_DIR / "fusion_weights.js"
    with open(path, "w") as f:
        f.write(js_code)
    logger.info("  Saved: %s", path)
    return js_code


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> dict:
    """Export all trained artifacts to JS-compatible format."""
    logger.info("MLAF Training Pipeline — Export to JavaScript")

    results: dict[str, str | None] = {}

    classifier_path = export_gesture_classifier()
    results["gesture_classifier"] = str(classifier_path) if classifier_path else None

    uasam_js = export_uasam_matrix()
    results["uasam_emission"] = "exported" if uasam_js else None

    gaze_js = export_gaze_matrix()
    results["gaze_emission"] = "exported" if gaze_js else None

    fusion_js = export_fusion_weights()
    results["fusion_weights"] = "exported" if fusion_js else None

    exported = sum(1 for v in results.values() if v is not None)
    logger.info("Export complete: %d / %d artifacts", exported, len(results))

    # Print summary for copy-paste into frontend
    print("\n" + "=" * 60)
    print("  JS Export Summary")
    print("=" * 60)
    for key, val in results.items():
        status = "OK" if val else "SKIPPED"
        print(f"  {key:<25} {status}")
    print("=" * 60)

    if classifier_path:
        size = classifier_path.stat().st_size / (1024 * 1024)
        print(f"\n  Classifier: {classifier_path} ({size:.2f} MB)")
    print(f"  Emission matrices: {MODELS_DIR / 'uasam_emission.js'}")
    print(f"  Gaze matrices: {MODELS_DIR / 'gaze_emission.js'}")
    print(f"  Fusion weights: {MODELS_DIR / 'fusion_weights.js'}")

    return results


if __name__ == "__main__":
    main()
    sys.exit(0)
